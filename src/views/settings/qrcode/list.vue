<template>
  <c-view>
    <template v-slot:header>
      <div class="title">
        {{ $route.meta.name || $t(`route.${$route.meta.title}`) }}
        <el-button type="primary" :size="size" icon="el-icon-plus" @click="showDialog">新增</el-button>
      </div>
    </template>
    <div class="main__box">
      <c-table
        selection
        hasBorder
        :max-height="685"
        :size="size"
        :loading="isLoading"
        :table-header="tableHeader"
        :table-list="tableList"
        :page-info="pageInfo"
        :table-inner-btns="tableInnerBtns"
        @change-pagination="changePagination"
      >
        <template v-slot:header>
          <el-form :inline="true" :model="searchObj" label-width="100px" class="search-form">
            <el-form-item label="二维码名称">
              <el-input
                v-model="searchObj.qrcodeName"
                class="search-item"
                :size="size"
                placeholder="二维码名称"
                clearable
              />
            </el-form-item>
            <el-form-item label="二维码编码">
              <el-input
                v-model="searchObj.qrcodeCode"
                class="search-item"
                :size="size"
                placeholder="二维码编码"
                clearable
              />
            </el-form-item>
            <el-form-item label="二维码生产者">
              <query-dict
                :dict-list="lobList"
                class="search-item"
                :size="size"
                placeholder="请选择"
                :value.sync="searchObj.producerCode"
              ></query-dict>
            </el-form-item>
            <el-form-item label="二维码使用者">
              <query-dict
                :multiple=true
                :dict-list="lobList"
                class="search-item"
                :size="size"
                placeholder="请选择"
                :value.sync="searchObj.userCode"
              ></query-dict>
            </el-form-item>
            <el-form-item label="审核状态">
              <el-select
                v-model="searchObj.status"
                :size="size"
                class="search-item"
                placeholder="请选择"
                clearable
              >
                <el-option
                  v-for="item in statusList"
                  :key="item.value"
                  :label="item.label"
                  :value="item.value"
                ></el-option>
              </el-select>
            </el-form-item>
            <el-form-item>
              <el-button
                type="primary"
                class="search-btn"
                :size="size"
                icon="el-icon-search"
                @click="searchSubmit"
              >查询</el-button>
            </el-form-item>
          </el-form>
        </template>
      </c-table>
    </div>

    <div v-if="dialogObj.isShow">
      <c-dialog
        :is-show="dialogObj.isShow"
        :title="dialogObj.title"
        close-btn
        @before-close="dialogObj.isShow = false"
        @on-submit="dialogConfirm"
      >
        <qrcode-add ref="childRef" :init-data.sync="dialogObj.initData"></qrcode-add>
      </c-dialog>
    </div>
  </c-view>
</template>

<script>
import mixinTable from 'mixins/table'
import CDialog from 'components/dialog'
import QrcodeAdd from './add'
import dictObj from '@/store/dictData'

export default {
  name: 'settingsQrcode',
  mixins: [mixinTable],
  components: {
    CDialog,
    QrcodeAdd
  },
  data(vm) {
    return {
      dialogObj: {}, // 弹窗数据
      lobList: dictObj.lobList, // 业务线集合
      statusList: dictObj.auditStatus, // 状态
      searchObj: {},
      tableInnerBtns: [
        {
          width: 200,
          prop: {
            name: 'status', // 为0或1
            toggle: [{
              icon: 'el-icon-check',
              title: '已审核'
            }, {
              icon: 'el-icon-close',
              title: '未审核'
            }]
          },
          handle(row) {
            const { status, id, qrcodeName } = row
            const updateStatus = status === 0 ? 1 : 0
            const updateMsg = status === 0 ? '审核不通过' : '审核通过'
            vm.confirmTip(`确认${updateMsg}【${qrcodeName}】二维码`, () => {
              vm.changeStatus({ id, status: updateStatus })
            })
          }
        },
        {
          name: '编辑',
          icon: 'el-icon-edit',
          handle(row) {
            const { id, contextKey, description, qrcodeCode, qrcodeName, producerCode, userCode } = row
            let contextKeyList = contextKey.split(',').map((item) => {
              return { value: item }
            })
            let userCodeList = userCode && userCode.split(',')
            vm.showDialog({
              title: '编辑二维码',
              initData: {
                id,
                contextKey: contextKeyList,
                qrcodeCode,
                qrcodeName,
                producerCode,
                userCode: userCodeList,
                description
              }
            })
          }
        },
        {
          name: '删除',
          icon: 'el-icon-delete',
          handle(row) {
            const { qrcodeName, id } = row
            vm.confirmTip(`是否删除【${qrcodeName}】二维码`, () => {
              vm.deleteQrcode(id)
            })
          }
        }
      ],
      tableHeader: [
        {
          label: '二维码ID',
          prop: 'id',
          fixed: true
        },
        {
          label: '二维码名称',
          prop: 'qrcodeName'
        },
        {
          label: '二维码编码',
          prop: 'qrcodeCode'
        },
        {
          label: '二维码使用者',
          prop: 'userCode'
        },
        {
          label: '审核状态',
          prop: 'status',
          formatter(row) {
            return row.status === 0 ? '未审核' : '已审核'
          }
        },
        {
          label: '字段值',
          prop: 'contextKey',
          width: 200
        },
        {
          label: '创建人',
          prop: 'opCreator'
        },
        {
          label: '创建时间',
          prop: 'created',
          width: 100
        },
        {
          label: '更新人',
          prop: 'opEditor'
        },
        {
          label: '更新时间',
          prop: 'updated',
          width: 100
        }
      ]
    }
  },
  created() {
    this.fetchData()
  },
  methods: {
    // 新增、编辑提交
    dialogConfirm() {
      const childRef = this.$refs.childRef
      const requestMethods = {
        'add': this.$api.qrcode.saveQrcode,
        'edit': this.$api.qrcode.updateQrcode
      }
      childRef.$refs.formRef.validate(valid => {
        if (valid) {
          const childFormModel = childRef.formModel
          const request = childFormModel.id ? requestMethods['edit'] : requestMethods['add']
          let { userCode, contextKey, qrcodeCode, ...other } = childFormModel
          let userCodeStr = userCode ? userCode.join(',') : '' // 使用者字段处理
          let contextKeyStr = contextKey.length && contextKey.map((item) => item.value).join(',') // 字段值处理
          request({
            ...other,
            qrcodeCode,
            userCode: userCodeStr,
            contextKey: contextKeyStr
          }).then(() => {
            this.responeHandle('操作成功')
          })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    },
    // 弹窗
    showDialog(opts) {
      this.dialogObj = {
        isShow: true,
        title: opts.title || '新增二维码',
        initData: opts.initData
      }
    },
    // 删除二维码
    deleteQrcode(id) {
      this.$api.qrcode.deleteQrcode({ id }).then(() => {
        this.$msgTip('删除成功')
        this.fetchData()
      })
    },
    // 启用、禁用
    changeStatus(params) {
      this.$api.qrcode.changeStatus(params).then(() => {
        this.$msgTip('更新成功')
        this.fetchData()
      })
    },
    fetchData() {
      const { pageSize, pageNo } = this.pageInfo
      const userCode = this.searchObj.userCode ? this.searchObj.userCode.join(',') : '' // 使用者字段
      this.isLoading = true
      this.$api.qrcode
        .getQrcodeList({
          ...this.searchObj,
          pageSize,
          pageNo,
          userCode
        })
        .then(res => {
          this.isLoading = false
          if (res.totalCount) {
            const { data, totalCount } = res
            this.pageInfo.totalNum = totalCount
            this.tableList = data || []
          } else {
            this.tableList = res || []
          }
        })
    }
  }
}
</script>

<style lang='less' scoped>
.title {
  width: 100%;
  display: flex;
  justify-content: space-between;
}
</style>
